package model.competicion;

public class PuntoIntermedioDto {
	
	public String id;
	public String idCompeticion;
	public String distanciaSalida;
	public int tiempoMaximo;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIdCompeticion() {
		return idCompeticion;
	}
	public void setIdCompeticion(String idCompeticion) {
		this.idCompeticion = idCompeticion;
	}
	public String getDistanciaSalida() {
		return distanciaSalida;
	}
	public void setDistanciaSalida(String distanciaSalida) {
		this.distanciaSalida = distanciaSalida;
	}
	public int getTiempoMaximo() {
		return tiempoMaximo;
	}
	public void setTiempoMaximo(int tiempoMaximo) {
		this.tiempoMaximo = tiempoMaximo;
	}
	
	


}
