package model.competicion;

public class PuntoIntermedioClasficacionDto {
	
	public String idPuntoIntermedio;
	public String idCompeticion;
	public String emailAtleta;
	public int tiempo;
	
	public String getIdPuntoIntermedio() {
		return idPuntoIntermedio;
	}
	public void setIdPuntoIntermedio(String idPuntoIntermedio) {
		this.idPuntoIntermedio = idPuntoIntermedio;
	}
	public String getIdCompeticion() {
		return idCompeticion;
	}
	public void setIdCompeticion(String idCompeticion) {
		this.idCompeticion = idCompeticion;
	}
	public String getEmailAtleta() {
		return emailAtleta;
	}
	public void setEmailAtleta(String emailAtleta) {
		this.emailAtleta = emailAtleta;
	}
	public int getTiempo() {
		return tiempo;
	}
	public void setTiempo(int tiempo) {
		this.tiempo = tiempo;
	}
	

	


}
